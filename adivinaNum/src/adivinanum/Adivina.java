    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
          
        int numero=Integer.parseInt(txtIntento.getText());
        
        
        if(numero==numRandom){
            JOptionPane.showMessageDialog(rootPane, "FELICIDADES ADIVINASTE!");
             txtIntento.setEnabled(false);
             lblMensaje.setText("Correcto");                               
        }                                  
        else if (numero > numRandom) {
            lblMensaje.setText("Demasiado Alto");
            getContentPane().setBackground(new java.awt.Color(0,51,255));//azul
             
        }else {
           lblMensaje.setText("Demasiado Bajo");
           getContentPane().setBackground(new java.awt.Color(0,51,255));//azul
        }
         
        
        if(numero<(numRandom+10)&&numero>(numRandom-10)){
            getContentPane().setBackground(new java.awt.Color(204,0,0));//rojo
        }
        
                     
    }//GEN-LAST:event_btnOKActionPerformed

